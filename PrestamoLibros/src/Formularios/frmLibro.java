    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (index==-1) {
            TListaLibros.add(new Libro(txtCodigo.getText(),cbTipo.getSelectedItem().toString(), cbCategorias.getSelectedItem().toString(), txtEditorial.getText(), txtNombre.getText(), txtAutor.getText(), txtanioPublicacion.getText()));
        }else{
            Libro obj = TListaLibros.get(index); 
            obj.setCodigo(txtCodigo.getText());
            obj.setTipo(cbTipo.getSelectedItem().toString());
            obj.setCategoria(cbCategorias.getSelectedItem().toString());
            obj.setEditorial( txtEditorial.getText());
            obj.setNombre(txtNombre.getText());
            obj.setAutor(txtAutor.getText());
            obj.setAnioPublicacion(txtanioPublicacion.getText());
            TListaLibros.modificar(obj, index);
        }
        frmPrincipal.MostrarDatos();
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

