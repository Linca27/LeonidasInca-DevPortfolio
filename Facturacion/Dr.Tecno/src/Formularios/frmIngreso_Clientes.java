    public Clientes CrearObjeto()
    {
        int cedula= Integer.valueOf(jTextField1.getText());
        String nombre = jTextField2.getText();
        int celular = Integer.valueOf(jTextField3.getText());
        String direccion = jTextField4.getText();
        
        Clientes ot= new Clientes(cedula, nombre, celular, direccion);
        return ot;
    }
    
    public void SetDatos(Clientes nn)
    {
        jTextField1.setText(String.valueOf(nn.getCedula()));
        jTextField2.setText(String.valueOf(nn.getNombre()));
        jTextField3.setText(String.valueOf(nn.getCelular()));
        jTextField4.setText(String.valueOf(nn.getDireccion()));
        
    }
    public String mensaje = " ";
    public boolean validarDatos(){
        if(jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty() 
      || jTextField3.getText().isEmpty() || jTextField4.getText().isEmpty()){
            return false;
        }
        return true;
    }
    
    public void Guardar(){
        
        try{
            
            if(validarDatos()){
                mensaje = "datos correctos";
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(null, "Faltan por ingresar datos");
            }
            
        }catch(Exception e){
            e.printStackTrace();
        }
            //JOptionPane.showMessageDialog(null, e.getMessage());
        }
